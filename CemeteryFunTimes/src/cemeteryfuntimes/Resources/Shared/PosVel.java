public class PosVel {
  
    protected float xPos;   
    protected float yPos;
    protected float xVel;
    protected float yVel;
    protected float xRad;
    protected float yRad;
    protected float rad;
    
    public float xPos() {
        return xPos;
    }
    public float yPos() {
        return yPos;
    }
    public float xVel() {
        return xVel;
    }
    public float yVel() {
        return yVel;
    }
    public float xRad() {
        return xRad;
    }
    public float yRad() {
        return yRad;
    }
    public float rad() {
        return rad;
    }
    
}